#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	a;

	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write (fd, "-", 1);
		n = n * -1;
	}
	if (n == 0)
	{
		write (fd, "0", 1);
		return ;
	}
	if (n >= 1 && n <= 9)
		a = n + 48;
	else if (n != 0)
	{
		ft_putnbr_fd(n / 10, fd);
		a = (n % 10) + 48;
	}
	write (fd, &a, 1);
}
